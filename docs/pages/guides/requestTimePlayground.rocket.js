import { html } from 'lit';
import { ssrRender } from '@rocket/js/ssr.js';
import { atlasDocLayout } from '@rocket/js/layouts/atlasDoc.js';
export { atlasDocComponents as components } from '@rocket/js/layouts/atlasDoc.js';
import { globalData } from '../globalData.js';
import { findComponent } from './requestTimeDemoCatalog.js';

/** @type {import('@rocket/js/types.js').JsPage} */
export default async (request, context) => {
  const component = findComponent(context.params.componentName);

  if (!component) {
    return new Response('Component not found', { status: 404 });
  }

  const url = new URL(request.url);
  const apiPath = `/request-time-javascript-pages/demo/api/components/${component.slug}.json`;
  const imagePath = `/request-time-javascript-pages/demo/open-graph/${component.slug}.svg`;

  context.pageData.title = `${component.name} Playground`;
  context.pageData.content = html`
    <h1>${component.name} Playground</h1>
    <p>${component.summary}</p>

    <h2>Variants</h2>
    <ul>
      ${component.variants.map(variant => html`<li>${variant}</li>`)}
    </ul>

    <h2>Related request-time outputs</h2>
    <ul>
      <li><a href=${apiPath}>Component JSON</a></li>
      <li><a href=${imagePath}>Generated SVG image</a></li>
    </ul>

    <p>Rendered for <code>${url.pathname}</code>.</p>
  `;

  return ssrRender(atlasDocLayout(context.pageData, globalData));
};

export const config = {
  path: '/request-time-javascript-pages/demo/playground/:componentName',
  metadata: {
    title: 'Request-time Guide Playground Demo',
    description: 'Parameterized HTML generated by a server-rendered JavaScript Page.',
  },
  render: 'server',
  menu: false,
};
