import { findComponent } from './requestTimeDemoCatalog.js';

/** @type {import('@rocket/js/types.js').JsPage} */
export default async (_request, { params }) => {
  const componentName = params.componentName || '';
  const component = findComponent(componentName);

  if (!component) {
    return Response.json(
      {
        error: 'Component not found',
        componentName,
      },
      { status: 404 },
    );
  }

  return {
    demo: 'parameterized JSON detail',
    ...component,
    slug: componentName,
  };
};

export const config = {
  path: '/request-time-javascript-pages/demo/api/components/:componentName.json',
  metadata: {
    title: 'Request-time Guide Component API Demo',
    description: 'Parameterized JSON output generated by a server-rendered JavaScript Page.',
  },
  render: 'server',
  menu: false,
};
