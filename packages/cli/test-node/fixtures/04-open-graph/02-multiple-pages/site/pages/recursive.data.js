import { PageTree } from '@rocket/engine';
import { html } from 'lit';

export const layout = data => html`
  <!DOCTYPE html>
  <html lang="en">
    <head>
      <meta charset="utf-8" />
    </head>
    <body>
      ${data.content()}
    </body>
  </html>
`;

const pageTree = new PageTree();
await pageTree.restore(new URL('./pageTreeData.rocketGenerated.json', import.meta.url));

export const openGraphLayout = data => html`
  <!DOCTYPE html>
  <html lang="en">
    <head>
      <meta charset="utf-8" />
    </head>
    <body>
      <h1>${pageTree.getPage(data.sourceRelativeFilePath)?.model?.name}</h1>
      <footer>Generated by <a href="https://rocket.modern-web.dev/">Rocket</a></footer>
    </body>
  </html>
`;

export { html };
